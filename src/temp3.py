import re

# 修正后的正则表达式
age_pattern = r"""(?xi)
^  # 确保修饰符生效
(?:
    # 情况1: 关键词在前
    (?:年[龄纪]|岁)
    [\s：:]* 
    (?:约|大概)? 
    (
        (?P<num1>\d{1,2})
        | 
        (?P<cn1>[零一二两三四五六七八九十廿卅]+)
    )
    |
    # 情况2: 数值在前
    (
        (?P<num2>\d{1,2})
        | 
        (?P<cn2>[零一二两三四五六七八九十廿卅]+)
    )
    [\s：:]* 
    (?:约|大概)? 
    (?:个?[周岁]|岁)
)
(?:多|余|左右)?  # 模糊词统一放在最后
"""

x = re.compile(age_pattern)
y = '30岁失业怎么办'
z = x.search(y)
print(z)  # 输出: {'num1': None, 'cn1': None, 'num2': '30', 'cn2': None, ...}
